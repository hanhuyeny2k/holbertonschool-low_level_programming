/**
 * op_add - add two nums
 * @a: first number
 * @b: second num
 * Return: result
  */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - add two nums
 * @a: first number
 * @b: second num
 * Return: result
  */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - add two nums
 * @a: first number
 * @b: second num
 * Return: result
  */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - add two nums
 * @a: first number
 * @b: second num
 * Return: result
  */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - add two nums
 * @a: first number
 * @b: second num
 * Return: result
  */
int op_mod(int a, int b)
{
	return (a % b);
}
